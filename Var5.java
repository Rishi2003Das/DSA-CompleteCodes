import java.util.*;
class Var5{
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter the first Float value: ");
        float a=sc.nextFloat();
        System.out.println("Enter the second Float value: ");
        float b=sc.nextFloat();
        System.out.println("The Sum value is: "+(a+b));
        System.out.println("The Difference value is: "+(a-b));
        System.out.println("The Multiplication value is: "+(a*b));
        System.out.println("The quotient value is: "+(a/b));
    }
}