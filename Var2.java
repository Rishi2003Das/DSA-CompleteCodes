import java.util.*;
class Var2{
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter the first integer value: ");
        int a=sc.nextInt();
        System.out.println("Enter the second integer value: ");
        int b=sc.nextInt();
        System.out.println("The Sum value is: "+(a+b));
        System.out.println("The Difference value is: "+(a-b));
        System.out.println("The Multiplication value is: "+(a*b));
        System.out.println("The quotient value is: "+(a/b));
    }
}